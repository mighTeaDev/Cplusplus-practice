#include <iostream>
#include <string>
#include <cmath>

//for loops execute a block of code a certain amount of times
//for(int i = *beginning point, usually 1*; i <+ *limit*; i *what to do at end of each loop -i++- *)

int main()
{
    for(int i = 10; i >+ 0; i--)
    {
        std::cout << i << '\n';
    }
    
    std::cout << "Happy New Year!\n";
return 0;
}